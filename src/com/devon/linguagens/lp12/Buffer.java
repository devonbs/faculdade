package com.devon.linguagens.lp12;

public class Buffer {
	
	private int numero;
	
	private int numeros[];

	public Buffer(int numero) {
		super();
		this.numero = numero;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public int[] getNumeros() {
		return numeros;
	}

	public void setNumeros(int[] numeros) {
		this.numeros = numeros;
	}

	
	
	
	
}
